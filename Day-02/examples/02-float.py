# Float variables
num1 = 8.0
num2 = 2.5

# Basic Arithmetic
result1 = num1 + num2
print("Addition:", result1)

result2 = num1 - num2
print("Subtraction:", result2)

result3 = num1 * num2
print("Multiplication:", result3)

result4 = num1 / num2
print("Division:", result4)

result6 = num1 // num2  # Floor division means integer division meaning the result is rounded down to the nearest whole number
print("Floor Division:", result6)

result7 = num1 % num2   # Modulus
print("Modulus:", result7)

# Rounding
result5 = round(3.14159265359, 3)  # Rounds to 2 decimal places
print("Rounded:", result5)
