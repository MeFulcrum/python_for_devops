# Integer variables
num1 = 19.5
num2 = 5

# Integer Division means dividing two integers and getting the whole number part of the quotient
result1 = num1 // num2
print("Integer Division:", result1)

# Modulus (Remainder)
result2 = num1 % num2
print("Modulus (Remainder):", result2)

# Absolute Value
result3 = abs(-7.25)
print("Absolute Value:", result3)
