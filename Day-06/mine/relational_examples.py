"""Relational operators examples (Day-06 notes)

Demonstrates comparison operators with a=10 and b=5.
Run: python relational_examples.py
"""

from __future__ import annotations


def main() -> None:
    a = 10
    b = 5
    print("Relational operators demo (a=10, b=5)")
    print(f"a == b -> {a == b}")
    print(f"a != b -> {a != b}")
    print(f"a > b -> {a > b}")
    print(f"a < b -> {a < b}")
    print(f"a >= b -> {a >= b}")
    print(f"a <= b -> {a <= b}")


if __name__ == '__main__':
    main()
